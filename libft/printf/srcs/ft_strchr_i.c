int	ft_strchr_i(const char *s, int c)
{
	int	i;

	if (c == 0)
		return (-1);
	i = 0;
	while (s[i] != (unsigned char)c)
	{
		if (!s[i])
			return (-1);
		i++;
	}
	return (i);
}
